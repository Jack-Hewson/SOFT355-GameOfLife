suite("Testing user functionality", function () {
    test("Updating click counter", async function () {
        var testUser1 = await logic.setPlayer("testUser1", "red");
        var oldValue = testUser1.clicks;

        await logic.setClick(testUser1.name);

        var testUser2 = await logic.getPlayer(testUser1.name);

        assert(oldValue === (testUser2.clicks - 1), 'Click counter is incorrect');

        await logic.removePlayer(testUser1.name);
    })

    test("Setting colour", function () {
        var colours = new Array();
        colours = ["red", "orange", "yellow", "green", "blue", "purple"];

        colours.forEach(async function each(colour) {
            var convert = await logic.getColour(colour);
            switch (convert) {
                case ("rgb(255, 0, 0)"):
                    assert(colour === "red", 'colour should be red');
                    break;
                case "rgb(255,165,0)":
                    assert(colour === "orange", 'colour should be orange');
                    break;
                case "rgb(255,255,0)":
                    assert(colour === "yellow", 'colour should be yellow');
                    break;
                case "rgb(0,128,0)":
                    assert(colour === "green", 'colour should be green');
                    break;
                case "rgb(0,0,255)":
                    assert(colour === "blue", 'colour should be blue');
                    break;
                case "rgb(128,0,128)":
                    assert(colour === "purple", 'colour should be purple');
                    break;
                default:
                    assert.fail("Colour conversion failed");
                    break;
            }
        })
    })

    test("Creating a new player", async function () {
        var testUser1 = await logic.setPlayer("testUser1","password123", "red");
        var testUser2 = await logic.getPlayer(testUser1.name);

        assert(testUser1.name === testUser2.name, "Player not created successfully");

        await logic.removePlayer(testUser1.name);
    })

    test("Saving with special character",function () {
        var testName = ["#testuser1", ".testuser1", "TESTMan!!", "test "];

        testName.forEach(async function each(name) {
            var testUser1 = await logic.setPlayer(name, "red");

            assert(testUser1 === false, "RegEx check unsuccessful");

            if (testUser1 === true)
                await logic.removePlayer(testName);
        })
    })

    test("Saving already used name", async function () {
        var testUser1 = await logic.setPlayer("testUser1", "red");
        var testUser2 = await logic.setPlayer("testUser1", "red");
        var testUser3 = await logic.setPlayer("testUser1", "blue");

        assert(testUser2 === false, "Should not be able to create a player of an already used name");
        assert(testUser3 === false, "Colour change should not impact if a player can be created");

        await logic.removePlayer(testUser1.name);

        if (testUser2 === true || testUser3 === true) {
            await logic.removePlayer(testUser3.name);
            await logic.removePlayer(testUser2.name);
        }
    })

    test("User login", async function () {
        var testUser1 = await logic.setPlayer("testUser1", "Hello123", "red");

        var success = await logic.getPlayerLogin("testUser1", "123Hello");
        await logic.removePlayer(testUser1.name);
        assert(success === false, "User able to login in with incorrect credentials");
    })
})